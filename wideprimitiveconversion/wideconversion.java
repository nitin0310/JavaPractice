package wideprimitiveconversion;

public class wideconversion {

	public static void main(String[] args) {
		
		System.out.print("H"+"e"+"l"); //act as string in double quotes
		System.out.println('l'+'o');// converted to int in single quotes and then added.
		
		System.out.print("H");
		System.out.print('e');
		System.out.print('y');
	}
}
